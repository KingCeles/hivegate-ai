from __future__ import annotations

import argparse
import csv
import shutil
import subprocess
import sys
from pathlib import Path

from ultralytics import YOLO


IMAGE_EXTENSIONS = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}
ROOT = Path(__file__).resolve().parent
DATASET_DIR = ROOT / "dataset"
LABELIMG_DIR = ROOT.parent / "labelImg-master"
DEFAULT_LABEL_DIR = DATASET_DIR / "label"
DEFAULT_CLASS_FILE = DATASET_DIR / "predefined_classes.txt"
DEFAULT_FRAME_INDEX = DATASET_DIR / "frame_index.csv"
SOURCE_DIRS = {
    "motion": DATASET_DIR / "motion_candidates",
    "sampled": DATASET_DIR / "sampled_frames",
}
CLASS_NAMES = ["bee"]


